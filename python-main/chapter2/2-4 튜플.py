# 튜플 생성
t1 = ()     #빈 튜플
t2 = (1,)   #요소가 1개일때는 ,로 끝내야함
t3 = (1, 2, 3)

# 튜플은 변경 불가
t = (1, 2, 'a', 'b')
# del t[0] #삭제시 에러
# t[0] = 'c' #수정시 에러

# 인덱스 사용 가능
t = (1, 2, 'a', 'b')
